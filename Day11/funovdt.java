import java.util.*;
public class funovdt
{
    public static int calsum(int n1,int n2)   //formal parameters
    {
return n1+n2;
    }
    public static float calsum(float n1,float n2)   //formal parameters
    {
return n1+n2;
    }
    public static void main(String args[])
    {
System.out.println(calsum(3,5));
System.out.println(calsum(3.2f,4.8f));
    }
}