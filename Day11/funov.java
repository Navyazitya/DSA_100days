import java.util.*;
public class funov
{
    public static int calsum(int n1,int n2)   //formal parameters
    {
return n1+n2;
    }
    public static int calsum(int n1,int n2,int n3)   //formal parameters
    {
return n1+n2+n3;
    }
    public static void main(String args[])
    {
System.out.println(calsum(3,5,2));
    }
}